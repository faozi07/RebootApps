package com.example.rebootapps;

public class ItemModel {
    private int image;
    private String nama;

    public ItemModel() {
    }

    public ItemModel(int image, String nama) {
        this.image = image;
        this.nama = nama;
    }

    public int getImage() {
        return image;
    }

    public String getNama() {
        return nama;
    }


}
